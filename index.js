function add(a, b){
    let sum = a + b;
    return sum;
}

function subtract (a, b){
    let difference = a - b;
    return difference;
}

function multiply (a, b){
    let mult = a * b;
    return mult;
}

function divide (a, b){
    let divi = a / b;
    return divi;
}

function increment(n){
    let incd = ++n;
    return incd;
}

function decrement(n){
    let decd = --n;
    return decd;
}

function makeInt(n){
    let parsd = parseInt(n,10);
    return parsd;
}

function preserveDecimal(n){
    let parsd = parseFloat(n)
    return parsd;
}